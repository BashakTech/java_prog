public class ArithmeticOperator {
	public static void main(String[] args) {
		int a, b, result;
		a = 5;
		b = 10;
		//result = a + b;

		System.out.println("a + b = " +(result=a+b));
		System.out.println("a - b = " +(result=a-b));
		System.out.println("a * b = " +(result=a*b));
		System.out.println("a / b = " +(result=a/b));
		System.out.println("a % b = " +(result=a%b));
		
	}
}